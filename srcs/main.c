#include "cub3d.h"

int main(int ac, char **av)
{
	t_gm	gm;

	c3d_init_gm(&gm);
	c3d_check_file(&gm, ac, av);
	c3d_hooks(&gm);
	mlx_loop(gm.mlx);
	c3d_success_exit();
	return (0);
}

